package prac;

public class Encap {

	private String name;
	private int age;
	private String id;
	private String city;

	public String getName() {
		return name;
	}

	public int getAge() {
		return age;
	}

	public String getID() {
		return id;
	}

	public String getCity() {
		return city;
	}

	public void setName(String name) {
		this.name = name;

	}

	public void setAge(int age) {
		this.age = age;
	}
	public void setID(String id) {
		this.id = id;
	}
	public void setCity(String city) {
		this.city = city;
	}

}
